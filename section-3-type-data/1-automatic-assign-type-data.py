# Categoria texto
# valor de tipo str 
name = "Johanna"
print(type(name)) # str

# Categoria numerica
# valor de tipo int
age = 35
print(type(age)) # int

# valor de tipo float
random_number = 35.5
print(type(random_number)) # float

# valor de tipo complex
complex_number = 35.5 + 45j
print(type(complex_number)) # complex

# Categoria secuencial
# valor de tipo list
food_list= ["macarrones", "patatas", "filete"]
print(type(food_list)) # list

# valor de tipo tuple
food_tuple = ("macarrones", "patatas", "filete")
print(type(food_tuple)) # tuple

# valor de tipo rango
number_range = range(8)
print(type(number_range)) # range

# Categoria map
# valor de tipo dict
food_dict = {1: "macarrones", 2: "patatas"}
print(type(food_dict)) # dict

# Conjuntos
# valor de tipo set
food_set = {"macarrones", "patatas", "filete"}
print(type(food_set)) # set

# Booleanos
# valor de tipo bool
bool_value = True
print(type(bool_value)) # bool